        /// <summary> 
        /// 对泛型列表的数据进行分页 
        /// </summary> 
        /// <typeparam name="T">列表的类型</typeparam> 
        /// <param name="obj">原始数据</param> 
        /// <param name="pagesize">每页尺寸</param> 
        /// <param name="pageindex">页码，引用型，当超过范围时会自动修正</param> 
        /// <param name="totalpage">传出，表示当前共有多少页</param> 
        /// <returns></returns> 
        public T[] GetObjectPage<T>(List<T> obj, int pagesize, ref int pageindex, out int totalpage)
        {
            T[] result;
            int count;

            if (pagesize < 1) throw new System.ArgumentOutOfRangeException("pagesize", "每页尺寸不得小于1");

            totalpage = (int)Math.Ceiling((double)obj.Count / pagesize);
            if (pageindex < 1) pageindex = 1;
            else if (pageindex > totalpage) pageindex = totalpage;

            count = pagesize;
            if (totalpage * pagesize > obj.Count) count = obj.Count - totalpage * (pagesize - 1);
            result = new T[count];

            obj.CopyTo(pagesize * (pageindex - 1), result, 0, count);

            return result;
        }
